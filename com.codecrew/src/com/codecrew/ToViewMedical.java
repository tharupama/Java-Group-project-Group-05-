    private void viewMedicalSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewMedicalSearchButtonActionPerformed
        
        String stId = viewMedicalStudentId.getText().trim();
        String status = viewMedicalStatus.getSelectedItem().toString();

        if (stId.isEmpty()) stId = null;

        String sql = "SELECT * FROM tomedical_record WHERE (ST_Id = ? OR ? IS NULL) AND (Status = ? OR ? = 'All')";
        
        try (
         Connection con = ToConnect.getConnection();
         PreparedStatement pst = con.prepareStatement(sql)) {

        pst.setString(1, stId);
        pst.setString(2, stId);
        pst.setString(3, status);
        pst.setString(4, status);

        ResultSet rs = pst.executeQuery();

        DefaultTableModel model = (DefaultTableModel) viewMedicalTable.getModel();
        model.setRowCount(0);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("Record_Id"),
                rs.getString("ST_Id"),
                rs.getInt("Session_Id"),
                rs.getDate("Date_Submit"),
                rs.getString("Reason"),
                rs.getString("Status"),
                rs.getString("Approved_By"),
                rs.getDate("Approved_Date")
            });
        }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_viewMedicalSearchButtonActionPerformed

