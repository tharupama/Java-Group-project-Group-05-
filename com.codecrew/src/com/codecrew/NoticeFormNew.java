    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       String noticeId = txtNoticeId.getText().trim();
           if (noticeId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Notice ID!");
            return;
        }
           
            int confirm = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to delete this notice?",
            "Delete Notice",
            JOptionPane.YES_NO_OPTION
        );
         if (confirm == JOptionPane.YES_OPTION) {
            NoticeService ns = new NoticeService();
            boolean result = ns.deleteNotice(noticeId);

            if (result) {
                JOptionPane.showMessageDialog(this, "Notice Deleted!");
                loadNotices(); // refresh table
                clearFields();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to Delete!");
            }
        }    
    }//GEN-LAST:event_btnDeleteActionPerformed
