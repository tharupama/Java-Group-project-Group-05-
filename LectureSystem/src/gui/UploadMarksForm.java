    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
            try{
            String selected=cmbStudent.getSelectedItem().toString();
            String studentId=selected.split(" - ")[0];
            String coursCode=cmbCourse.getSelectedItem().toString();
            
            double quiz01=Double.parseDouble(txtQuiz1.getText());
            double quiz02=Double.parseDouble(txtQuiz2.getText());
            double quiz03=Double.parseDouble(txtQuiz3.getText());  
            
            double assessment=Double.parseDouble(txtAssessment.getText());
            double midTheory=Double.parseDouble(txtMidTheory.getText());
            double midPractical=Double.parseDouble(txtMidPractical.getText());
            
            double finalTheory=Double.parseDouble(txtFinalTheory.getText());
            double finalractical=Double.parseDouble(txtFinalPractical.getText());            
            
            String type="";
            if(rbTheory.isSelected()){
                type="Theory";
            }else if(rbPractical.isSelected()){
                type="Practical";
            }else if(rbBoth.isSelected()){
                type="Both";
            }
            MarksService ms=new MarksService();
            boolean result=ms.saveMarks(studentId, coursCode, quiz01, quiz02, quiz03, assessment, midTheory, midPractical, finalTheory, finalractical, type);
            
            if(result){
              JOptionPane.showMessageDialog(this,"Marks Updated Successfully!");
              
            }else{
                JOptionPane.showMessageDialog(this,"Failed to Update Marks!");
            }
            }catch(Exception e){
                System.out.println(e.getMessage());
            }
    }//GEN-LAST:event_btnSaveActionPerformed

