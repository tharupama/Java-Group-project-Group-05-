    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String noticeId=txtNoticeId.getText().trim();
        String title=txtTitle.getText().trim();
        String description = txtDescription.getText().trim();
        String postedBy = txtPostedBy.getText().trim();
        
    if (noticeId.isEmpty() || title.isEmpty() || 
            description.isEmpty() || postedBy.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields!");
            return;
        }  
      NoticeService ns = new NoticeService();
      boolean result= ns.addNotice(noticeId, title, description, postedBy);
      if(result){
         JOptionPane.showMessageDialog(this, "Notice Added Successfully!"); 
         loadNotices();
         clearFields();   
      }else{
        JOptionPane.showMessageDialog(this, "Failed to Add Notice!");
      }
      
        
    }//GEN-LAST:event_btnAddActionPerformed

